package student;

public class Student {
    private String name;
    private int id;
    
    public String getName()
    {
        return name;
    }
    
    public int getId()
    {
        return id;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
   

}

